                                The popularity of the object-oriented term has grown so much that the phrase object-oriented has become something of a catch-phrase for all that is good in Computing, expanding out of the programming language field and influencing (as an example) software engineering and databases.
This course attempts to obtain a deep understanding of object oriented concepts and to examine various application of object oriented methodologies. We will read and discuss papers about different topics within OO, including inheritence and method dispatch techniques, reasoning and typing techniques, frameworks and library design and language evolution.
